/* ── AXP192 ───────────────────────────────────────────────── */
static void axp192_write(uint8_t reg, uint8_t val)
{
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (AXP192_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd, reg, true);
    i2c_master_write_byte(cmd, val, true);
    i2c_master_stop(cmd);
    i2c_master_cmd_begin(I2C_NUM_0, cmd, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(cmd);
}

static void axp192_init(void)
{
    i2c_config_t conf = {
        .mode             = I2C_MODE_MASTER,
        .sda_io_num       = AXP_SDA,
        .scl_io_num       = AXP_SCL,
        .sda_pullup_en    = GPIO_PULLUP_ENABLE,
        .scl_pullup_en    = GPIO_PULLUP_ENABLE,
        .master.clk_speed = 400000,
    };
    i2c_param_config(I2C_NUM_0, &conf);
    i2c_driver_install(I2C_NUM_0, I2C_MODE_MASTER, 0, 0, 0);

    axp192_write(0x10, 0xFF); // OLED VPP enable
    axp192_write(0x28, 0xFF); // LDO2 & LDO3 — LCD + SD power
    axp192_write(0x82, 0xFF); // Enable all ADCs
    axp192_write(0x33, 0xC0); // Enable charging
    axp192_write(0xB8, 0x80); // Enable Coulomb counter
    axp192_write(0x12, 0x4D); // Enable DC-DC1, LDO2, LDO3
    axp192_write(0x36, 0x5C); // PEK settings
    axp192_write(0x90, 0x02); // GPIO0 = LDO → backlight ON

    ESP_LOGI(TAG, "AXP192 init OK");
}

/* ── internal: flush region to panel ─────────────────────── */
static void flush_region(int x1, int y1, int x2, int y2)
{
    esp_lcd_panel_draw_bitmap(s_panel, x1, y1, x2 + 1, y2 + 1,
                              s_fb + y1 * LCD_W + x1);
}

/* ── public: init ─────────────────────────────────────────── */
esp_err_t display_init(void)
{
    // Power on display via AXP192
    axp192_init();

    // SPI bus
    spi_bus_config_t buscfg = {
        .mosi_io_num     = LCD_MOSI,
        .miso_io_num     = -1,
        .sclk_io_num     = LCD_SCLK,
        .quadwp_io_num   = -1,
        .quadhd_io_num   = -1,
        .max_transfer_sz = LCD_W * LCD_H * 2,
    };
    ESP_ERROR_CHECK(spi_bus_initialize(SPI2_HOST, &buscfg, SPI_DMA_CH_AUTO));

    // LCD IO
    esp_lcd_panel_io_spi_config_t io_cfg = {
        .dc_gpio_num       = LCD_DC,
        .cs_gpio_num       = LCD_CS,
        .pclk_hz           = LCD_SPI_HZ,
        .lcd_cmd_bits      = 8,
        .lcd_param_bits    = 8,
        .spi_mode          = 0,
        .trans_queue_depth = 10,
    };
    ESP_ERROR_CHECK(esp_lcd_new_panel_io_spi(SPI2_HOST, &io_cfg, &s_io));

    // ST7789 panel
    esp_lcd_panel_dev_config_t panel_cfg = {
        .reset_gpio_num = LCD_RST,
        .rgb_endian     = LCD_RGB_ENDIAN_RGB,
        .bits_per_pixel = 16,
    };
    ESP_ERROR_CHECK(esp_lcd_new_panel_st7789(s_io, &panel_cfg, &s_panel));

    esp_lcd_panel_reset(s_panel);
    esp_lcd_panel_init(s_panel);
    esp_lcd_panel_invert_color(s_panel, true);  // required for M5StickC Plus
    esp_lcd_panel_set_gap(s_panel, LCD_X_OFFSET, LCD_Y_OFFSET);
    esp_lcd_panel_disp_on_off(s_panel, true);

    display_clear(COLOR_BLACK);
    ESP_LOGI(TAG, "Display init OK (%dx%d)", LCD_W, LCD_H);
    return ESP_OK;
}

/* ── public: clear screen ─────────────────────────────────── */
void display_clear(uint16_t color)
{
    for (int i = 0; i < LCD_W * LCD_H; i++) s_fb[i] = color;
    esp_lcd_panel_draw_bitmap(s_panel, 0, 0, LCD_W, LCD_H, s_fb);
}

/* ── public: print text ───────────────────────────────────── */
void display_print(int x, int y, const char *text, uint16_t fg, uint16_t bg)
{
    while (*text) {
        char c = *text++;
        if (c < 32 || c > 'z') c = ' ';
        const uint8_t *glyph = font5x7[c - 32];

        for (int col = 0; col < 5; col++) {
            for (int row = 0; row < 7; row++) {
                int px = x + col;
                int py = y + row;
                if (px < LCD_W && py < LCD_H) {
                    s_fb[py * LCD_W + px] = (glyph[col] & (1 << row)) ? fg : bg;
                }
            }
        }
        x += 6; // 5px char + 1px spacing
        if (x + 6 > LCD_W) break;
    }
    flush_region(0, y, LCD_W - 1, y + 7);
}